"use client";

import { useState, useEffect } from "react";
import { Plus, Search, Filter, Edit2, Trash2, MoreVertical, Package, ExternalLink, Activity, AlertTriangle, Layers, DollarSign, LayoutGrid } from "lucide-react";
import { motion, AnimatePresence } from "framer-motion";
import ProductModal from "@/components/admin/ProductModal";

export default function AdminProductsPage() {
    const [products, setProducts] = useState<any[]>([]);
    const [loading, setLoading] = useState(true);
    const [isModalOpen, setIsModalOpen] = useState(false);
    const [editingProduct, setEditingProduct] = useState<any>(null);
    const [searchQuery, setSearchQuery] = useState("");
    const [viewMode, setViewMode] = useState<'grid' | 'list'>('list');

    const fetchProducts = async () => {
        try {
            const res = await fetch("/api/products");
            const data = await res.json();
            if (Array.isArray(data)) {
                setProducts(data);
            } else {
                console.error("Received invalid data format:", data);
                setProducts([]);
            }
        } catch (err) {
            console.error("Failed to fetch products", err);
            setProducts([]);
        } finally {
            setLoading(false);
        }
    };

    useEffect(() => {
        fetchProducts();
    }, []);

    const handleSave = async (formData: any) => {
        const url = editingProduct ? `/api/products/${editingProduct._id}` : "/api/products";
        const method = editingProduct ? "PUT" : "POST";

        try {
            const res = await fetch(url, {
                method,
                headers: { "Content-Type": "application/json" },
                body: JSON.stringify(formData),
            });

            if (res.ok) {
                fetchProducts();
            }
        } catch (err) {
            console.error("Failed to save product", err);
        }
    };

    const handleDelete = async (id: string) => {
        if (!confirm("Are you sure you want to delete this product?")) return;

        try {
            const res = await fetch(`/api/products/${id}`, { method: "DELETE" });
            if (res.ok) {
                fetchProducts();
            }
        } catch (err) {
            console.error("Failed to delete product", err);
        }
    };

    const filteredProducts = products.filter(p =>
        p.name.toLowerCase().includes(searchQuery.toLowerCase()) ||
        p.category.toLowerCase().includes(searchQuery.toLowerCase())
    );

    // KPI Calculations
    const totalProducts = products.length;
    const totalCategories = new Set(products.map(p => p.category)).size;
    const lowStockCount = products.filter(p => {
        const stock = p.variants?.reduce((acc: number, v: any) => acc + (v.stock || 0), 0) || p.stock || 0;
        return stock < 10 && stock > 0;
    }).length;

    // Derived logic for display
    const getStockStatus = (p: any) => {
        const stock = p.variants?.reduce((acc: number, v: any) => acc + (v.stock || 0), 0) || p.stock || 0;
        if (stock === 0) return { label: 'Out of Stock', color: 'bg-red-500', text: 'text-red-500', bg: 'bg-red-50' };
        if (stock < 10) return { label: 'Low Stock', color: 'bg-orange-500', text: 'text-orange-600', bg: 'bg-orange-50' };
        return { label: 'In Stock', color: 'bg-green-600', text: 'text-green-700', bg: 'bg-green-50' };
    };

    return (
        <div className="space-y-8 font-sans">
            {/* KPI Summary Cards */}
            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-6">
                {[
                    { label: "Total Products", value: totalProducts, icon: Package, color: "text-[#2F3E2C]" },
                    { label: "Total Categories", value: totalCategories, icon: Layers, color: "text-[#C6A75E]" },
                    { label: "Low Stock Items", value: lowStockCount, icon: AlertTriangle, color: "text-orange-500" },
                    { label: "Monthly Revenue", value: "₹45.2k", icon: Activity, color: "text-green-600" }, // Mocked
                ].map((kpi, i) => (
                    <motion.div
                        key={i}
                        initial={{ opacity: 0, y: 10 }}
                        animate={{ opacity: 1, y: 0 }}
                        transition={{ delay: i * 0.1 }}
                        className="bg-white p-6 rounded-[20px] shadow-sm border border-gray-100 flex items-center justify-between hover:shadow-md transition-shadow duration-300"
                    >
                        <div>
                            <p className="text-[11px] font-black uppercase tracking-widest text-gray-400 mb-1">{kpi.label}</p>
                            <h3 className="text-3xl font-serif font-bold text-[#2F3E2C]">{kpi.value}</h3>
                        </div>
                        <div className={`p-4 rounded-2xl bg-gray-50 ${kpi.color}`}>
                            <kpi.icon size={24} />
                        </div>
                    </motion.div>
                ))}
            </div>

            {/* Header Section */}
            <div className="flex flex-col md:flex-row justify-between items-end gap-6 pb-4 border-b border-[#2F3E2C]/5">
                <div>
                    <h1 className="text-4xl font-serif font-black text-[#2F3E2C] tracking-tight">Inventory Management</h1>
                    <p className="text-gray-400 mt-2 font-medium tracking-wide">Manage your product catalog, stock levels, and store inventory.</p>
                </div>
                <button
                    onClick={() => { setEditingProduct(null); setIsModalOpen(true); }}
                    className="bg-gradient-to-r from-[#C6A75E] to-[#D4B874] text-white px-8 py-4 rounded-xl flex items-center gap-3 font-bold uppercase tracking-wider text-xs shadow-lg shadow-[#C6A75E]/30 hover:shadow-xl hover:-translate-y-1 transition-all active:scale-95 group"
                >
                    <Plus size={18} strokeWidth={3} className="group-hover:rotate-90 transition-transform duration-300" />
                    Add New Product
                </button>
            </div>

            {/* Controls Bar */}
            <div className="flex flex-col md:flex-row gap-4 items-center justify-between sticky top-0 z-30 py-4 bg-[#F8F6F2]/80 backdrop-blur-md">
                <div className="relative w-full md:w-[480px] group">
                    <Search className="absolute left-5 top-1/2 -translate-y-1/2 text-gray-300 group-focus-within:text-[#C6A75E] transition-colors" size={20} />
                    <input
                        type="text"
                        placeholder="Search products by name, category..."
                        value={searchQuery}
                        onChange={(e) => setSearchQuery(e.target.value)}
                        className="w-full bg-white border border-gray-100 focus:border-[#C6A75E]/50 rounded-2xl py-4 pl-14 pr-6 outline-none shadow-sm transition-all font-medium text-[#2F3E2C] placeholder:text-gray-300"
                    />
                </div>
                <div className="flex gap-3 w-full md:w-auto">
                    <button className="flex-1 md:flex-none flex items-center justify-center gap-2 px-6 py-4 bg-white border border-gray-100 rounded-2xl font-bold text-gray-400 hover:text-[#2F3E2C] hover:border-[#2F3E2C]/20 transition-all shadow-sm uppercase tracking-widest text-[10px]">
                        <Filter size={16} /> Filter
                    </button>
                    <button className="flex-1 md:flex-none flex items-center justify-center gap-2 px-6 py-4 bg-white border border-gray-100 rounded-2xl font-bold text-gray-400 hover:text-[#2F3E2C] hover:border-[#2F3E2C]/20 transition-all shadow-sm uppercase tracking-widest text-[10px]">
                        <Package size={16} /> Manage UOMs
                    </button>
                    <div className="w-px h-10 bg-gray-200 mx-2 hidden md:block"></div>
                    <button
                        onClick={() => setViewMode(viewMode === 'list' ? 'grid' : 'list')}
                        className="p-4 bg-white border border-gray-100 rounded-2xl text-gray-400 hover:text-[#2F3E2C] transition-colors shadow-sm"
                    >
                        <LayoutGrid size={20} />
                    </button>
                </div>
            </div>

            {/* Product List - Card Style Rows */}
            <div className="space-y-4">
                {loading ? (
                    <div className="flex flex-col items-center justify-center py-20">
                        <div className="w-16 h-16 border-4 border-[#C6A75E] border-t-transparent rounded-full animate-spin mb-6" />
                        <p className="font-bold text-gray-400 uppercase tracking-widest text-xs">Loading Inventory...</p>
                    </div>
                ) : filteredProducts.length === 0 ? (
                    <div className="bg-white rounded-[2rem] p-12 text-center border-2 border-dashed border-gray-100">
                        <Package className="w-16 h-16 text-gray-200 mx-auto mb-4" />
                        <h3 className="text-xl font-serif font-bold text-[#2F3E2C] mb-2">No Products Found</h3>
                        <p className="text-gray-400 max-w-md mx-auto mb-8">We couldn't find any products matching your search. Try adjusting your filters or add a new product.</p>
                        <button onClick={() => setSearchQuery("")} className="text-[#C6A75E] font-bold hover:underline">Clear Search</button>
                    </div>
                ) : (
                    <AnimatePresence>
                        {filteredProducts.map((p, i) => {
                            const status = getStockStatus(p);
                            const totalStock = p.variants?.reduce((acc: number, v: any) => acc + (v.stock || 0), 0) || p.stock || 0;
                            const minPrice = p.variants?.length ? Math.min(...p.variants.map((v: any) => v.price)) : p.price;

                            return (
                                <motion.div
                                    key={p._id}
                                    initial={{ opacity: 0, y: 10 }}
                                    animate={{ opacity: 1, y: 0 }}
                                    exit={{ opacity: 0, scale: 0.98 }}
                                    transition={{ delay: i * 0.05 }}
                                    className="group bg-white rounded-[20px] p-4 border border-gray-100 hover:border-[#C6A75E]/30 hover:shadow-lg hover:shadow-[#C6A75E]/5 transition-all duration-300 flex flex-col md:flex-row items-center gap-6 relative overflow-hidden"
                                >
                                    {/* Decoration Line */}
                                    <div className="absolute left-0 top-0 bottom-0 w-1 bg-[#2F3E2C] opacity-0 group-hover:opacity-100 transition-opacity" />

                                    {/* Image */}
                                    <div className="w-full md:w-20 h-20 bg-[#F8F6F2] rounded-2xl overflow-hidden flex-shrink-0 relative group-hover:scale-105 transition-transform duration-500">
                                        {p.images?.[0] ?
                                            <img src={p.images[0]} className="w-full h-full object-cover" alt={p.name} /> :
                                            <div className="w-full h-full flex items-center justify-center text-[#2F3E2C]/20"><Package size={24} /></div>
                                        }
                                    </div>

                                    {/* Info */}
                                    <div className="flex-grow text-center md:text-left">
                                        <div className="flex flex-col md:flex-row md:items-center gap-2 mb-1">
                                            <h3 className="font-bold text-lg text-[#2F3E2C] leading-tight">{p.name}</h3>
                                            <span className="px-3 py-1 bg-[#F8F6F2] text-[#2F3E2C]/60 text-[10px] font-black uppercase tracking-widest rounded-full w-fit mx-auto md:mx-0 border border-[#2F3E2C]/5">
                                                {p.category}
                                            </span>
                                        </div>
                                        <div className="flex items-center justify-center md:justify-start gap-4 text-xs font-medium text-gray-400">
                                            <span>SKU: {p._id.substring(0, 6).toUpperCase()}</span>
                                            <span className="w-1 h-1 bg-gray-300 rounded-full" />
                                            <span>{p.variants?.length ? `${p.variants.length} Variants` : 'Single Item'}</span>
                                        </div>
                                    </div>

                                    {/* Metrics Grid */}
                                    <div className="flex items-center gap-8 md:gap-12 w-full md:w-auto justify-between md:justify-end px-4 md:px-0 border-t md:border-none border-gray-50 pt-4 md:pt-0">
                                        {/* Price */}
                                        <div className="text-center md:text-right">
                                            <p className="text-[10px] font-black text-gray-300 uppercase tracking-widest mb-1">Price</p>
                                            <p className="text-lg font-serif font-black text-[#2F3E2C]">₹{minPrice}</p>
                                        </div>

                                        {/* Stock Status */}
                                        <div className="text-center md:text-right min-w-[100px]">
                                            <p className="text-[10px] font-black text-gray-300 uppercase tracking-widest mb-1">Stock Level</p>
                                            <div className="flex items-center justify-center md:justify-end gap-2">
                                                <div className={`w-2 h-2 rounded-full ${status.color} animate-pulse`} />
                                                <span className={`font-bold text-sm ${status.text}`}>{totalStock}</span>
                                            </div>
                                        </div>

                                        {/* Actions */}
                                        <div className="flex gap-2">
                                            <button
                                                onClick={() => { setEditingProduct(p); setIsModalOpen(true); }}
                                                className="w-10 h-10 rounded-full flex items-center justify-center text-gray-400 hover:text-[#2F3E2C] hover:bg-[#F8F6F2] transition-colors border border-transparent hover:border-gray-200"
                                            >
                                                <Edit2 size={18} />
                                            </button>
                                            <button
                                                onClick={() => handleDelete(p._id)}
                                                className="w-10 h-10 rounded-full flex items-center justify-center text-gray-400 hover:text-red-500 hover:bg-red-50 transition-colors border border-transparent hover:border-red-100" // Red action for delete needs to signal caution
                                            >
                                                <Trash2 size={18} />
                                            </button>
                                        </div>
                                    </div>
                                </motion.div>
                            );
                        })}
                    </AnimatePresence>
                )}
            </div>

            <ProductModal
                isOpen={isModalOpen}
                onClose={() => setIsModalOpen(false)}
                onSave={handleSave}
                product={editingProduct}
            />
        </div>
    );
}
